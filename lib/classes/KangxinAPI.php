<?php

class KangxinAPI {
    private static $instance;
    private $endpoint;
    private $timeout = 30;
    /** @var int */
    private $delay;
    private $lastRequestTime = 0;
    private static $cache = [];

    private function __construct($endpoint, $timeout = null) {
        $this->endpoint = trim($endpoint, '/') . '/';
        if (is_numeric($timeout) && intval($timeout) > 0) {
            $this->timeout = intval($timeout);
        }
    }

    static public function getInstance() {
        if (!self::$instance) {
            self::$instance = new KangxinAPI($GLOBALS['KANGXIN_API_URL'], $GLOBALS['KANGXIN_API_TIMEOUT']);
        }
        return self::$instance;
    }

    public function setDelay($seconds) {
        $this->delay = $seconds;
    }

    /**
     * Invokes the Kangxin API to retrieve a list of patients that should be imported in the Linkcare platform
     *
     * @param string $fromDate
     * @param boolean $isFirstLoad
     * @throws ServiceException
     * @return KangxinPatientInfo[]
     */
    public function requestPatientList($fromDate, $isFirstLoad = false) {
        $waitTime = $this->delay - (microtime(true) - $this->lastRequestTime);
        if ($waitTime > 0) {
            usleep($waitTime * 1000000);
        }

        $filterParameter = $GLOBALS['USE_UPDATE_TIME_FILTER'] ? 'updateTime' : 'operationTime';
        if ($GLOBALS['SIMULATE_KANGXIN_API']) {
            $resp = $this->simulatedData();
        } else {
            $params[$filterParameter] = $fromDate;
            $resp = $this->invokeAPI('healthData/syncOpInfos', $params, false);
            if (is_array($resp) && $GLOBALS['ANONYMIZE_DATA']) {
                foreach ($resp as $info) {
                    self::anonymizeOperationData($info);
                }
            }
        }

        $this->lastRequestTime = microtime(true);

        if (!is_array($resp)) {
            throw new ServiceException(ErrorCodes::API_INVALID_DATA_FORMAT, 'healthData/opernInfos function should return an array as response');
        }

        $operations = [];
        $minAdmissionDate = null;
        $episodesFound = [];
        $operationsFound = [];
        foreach ($resp as $info) {
            $op = KangxinPatientInfo::fromJson($info);
            $operations[] = $op;
            if (!$minAdmissionDate || $op->getAdmissionTime() < $minAdmissionDate) {
                $minAdmissionDate = $op->getAdmissionTime();
            }
            $episodesFound[] = $op->getResidenceNo();
            $operationsFound[] = $op->getApplyOperatNo();
        }

        if (!$GLOBALS['SIMULATE_KANGXIN_API'] && $isFirstLoad && $minAdmissionDate) {
            /*
             * This is a special action that will only be executed the first time that we request records.
             * The API functions returns individual operations of an episode. For this reason, when fetching records for the first time, some episodes
             * may end up with an incomplete number of operations.
             * The reason is that we request individual operations from a certain date, but it may happen that a patient had a previous operation in
             * the same episode, and then we would not be receiving it.
             * We are going to ensure that we have all the information aobut the episodes
             */
            $params[$filterParameter] = $minAdmissionDate;
            $resp = $this->invokeAPI('healthData/syncOpInfos', $params, false);
            if (is_array($resp)) {
                foreach ($resp as $info) {
                    $op = KangxinPatientInfo::fromJson($info);
                    if (!$op || !in_array($op->getResidenceNo(), $episodesFound) || in_array($op->getApplyOperatNo(), $operationsFound)) {
                        // Not an operation of an episode that should be included in the response, or an already loaded operation
                        continue;
                    }
                    if ($op->getOperationDate() < $fromDate) {
                        // This is an operation that happened before the threshold date but corresponds to an episode in the response set
                        $operations[] = $op;
                    }
                }
            }
        }

        return $operations;
    }

    /**
     * Invokes a REST function in the Kangxin API
     *
     * @param string $function
     * @param string[] $params
     * @throws ServiceException
     * @return stdClass
     */
    private function invokeAPI($function, $params, $sendAsJson = true) {
        $this->httpStatus = null;

        $endpoint = $this->endpoint . $function;
        $errorMsg = null;
        $headers = [];

        if ($sendAsJson) {
            $headers[] = 'Content-Type: application/json';
            $params = json_encode($params);
        }

        $options = [CURLOPT_POST => true, CURLOPT_HEADER => false, CURLOPT_AUTOREFERER => true, CURLOPT_CONNECTTIMEOUT => 10,
                CURLOPT_HTTPHEADER => $headers, CURLOPT_TIMEOUT => $this->timeout, CURLOPT_POSTFIELDS => $params,
                CURLOPT_SSLVERSION => CURL_SSLVERSION_TLSv1_2, CURLOPT_SSL_VERIFYPEER => false, CURLOPT_SSL_VERIFYHOST => false,
                CURLOPT_RETURNTRANSFER => 1];
        $curl = curl_init($endpoint);
        curl_setopt_array($curl, $options);
        curl_setopt($curl, CURLOPT_HEADERFUNCTION, [$this, "handleHeaderLine"]);

        // Check if any error occurred
        if (curl_errno($curl)) {
            $errorMsg = 'CURL error: ' . curl_error($curl);
        }

        $APIResponse = curl_exec($curl);
        if ($APIResponse === false) {
            $errorMsg = 'CURL error: ' . curl_error($curl);
        }

        curl_close($curl);

        if ($errorMsg) {
            throw new ServiceException(ErrorCodes::API_COMM_ERROR, $errorMsg);
        }
        if (!$APIResponse && !startsWith('2', $this->httpStatus)) {
            // The did not provide any information but responded with an HTTP error status
            throw new ServiceException(ErrorCodes::API_ERROR_STATUS, $this->httpStatus);
        }
        return $this->parseAPIResponse($APIResponse);
    }

    /**
     * Handles header lines returned by a CURL command
     *
     * @param resource $curl
     * @param string $header_line
     * @return number
     */
    function handleHeaderLine($curl, $header_line) {
        $matches = null;
        if (preg_match('/^HTTP[^\s]*\s(\d+)*/', $header_line, $matches)) {
            $this->httpStatus = $matches[1];
        }
        return strlen($header_line);
    }

    /**
     * Parses the response of the Kangxin API and checks if any error was reported.
     * All responses from the API contain 3 fields:+
     * <ul>
     * <li>code: an error code (or empty if no error occurred)</li>
     * <li>message: Explanation of the error (if any)</li>
     * <li>result: Contents of the response of the API function</li>
     * </ul>
     * If an error is reported by the API, an exception will be thrown. Otherwise the function returns the contents of the response
     *
     * @param string $APIResponse
     * @throws ServiceException::
     * @return stdClass
     */
    private function parseAPIResponse($APIResponse) {
        $result = json_decode($APIResponse);

        if (!$result) {
            // Error decoding JSON format
            throw new ServiceException(ErrorCodes::API_INVALID_DATA_FORMAT);
        }

        $errorCode = $result->code;
        if (!isNullOrEmpty($errorCode) && $errorCode != 0) {
            // API returned an error message
            throw new ServiceException(ErrorCodes::API_FUNCTION_ERROR, trim($result->code . ': ' . $result->msg));
        }

        return $result->result;
    }

